    private void editarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarButtonActionPerformed
        if (veiculos.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum usuário cadastrado.");
        } else {
            Object[] possibleValues = veiculos.keySet().toArray();
            Object selectedValue = JOptionPane.showInputDialog(
                    this, "Escolha o usuário", "Escolha", JOptionPane.INFORMATION_MESSAGE,
                    null, possibleValues, possibleValues[0]);
            if (selectedValue != null) {
                CadastroVeiculo c = new CadastroVeiculo(this, veiculos.get(selectedValue.toString()));
                c.setVisible(true);

            } else {
                JOptionPane.showMessageDialog(this, "Nenhum usuário selecionado.");
            }
        }
    }//GEN-LAST:event_editarButtonActionPerformed

