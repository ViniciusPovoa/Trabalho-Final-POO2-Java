    private void visualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_visualizarButtonActionPerformed
 if (veiculos.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nenhum usuário cadastrado.");
    } else {
        Object[] possibleValues = veiculos.keySet().toArray();
        Object selectedValue = JOptionPane.showInputDialog(
            this, "Escolha o usuário", "Escolha", JOptionPane.INFORMATION_MESSAGE,
            null, possibleValues, possibleValues[0]);

        if (selectedValue != null) {
            // Pega o objeto carro que corresponde ao atual
            Veiculo carroSelecionado = veiculos.get(selectedValue.toString());

            PagamentoStrategy strategy;

            // Verifica o tipo de veículo e define a estratégia apropriada
            if (carroSelecionado instanceof Carro) {
                strategy = new CarroPagamentoStrategy();
            } else if (carroSelecionado instanceof Moto) {
                strategy = new MotoPagamentoStrategy();
            } else {
                // Caso padrão, defina uma estratégia padrão ou trate o erro de alguma forma
                strategy = null; // Defina a estratégia padrão ou trate o erro de outra forma
            }

            Visualizar visu = new Visualizar(veiculos, this, strategy);

              carroSelecionado.adicionarObservador(visu);

            
            visu.metodoVisualizar(
                carroSelecionado.getNomeCondutor(),
                carroSelecionado.getAutomovel(),
                carroSelecionado.getPlacaVeiculo(),
                carroSelecionado.getHoraEntrada()
            );

            visu.tempoUtilizado(carroSelecionado.getHoraEntrada());

            visu.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum usuário selecionado.");
        }
    }
    }//GEN-LAST:event_visualizarButtonActionPerformed

